package edu.mfvp.encryptpassword.security;

import edu.mfvp.encryptpassword.schemas.UserSchema;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

public class UserDetailsImpl implements UserDetails {
    private final UserSchema userSchema;

    public UserDetailsImpl(UserSchema userSchema) {
        this.userSchema = userSchema;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of();
    }

    @Override
    public String getPassword() {
        return userSchema.getPassword();
    }

    @Override
    public String getUsername() {
        return userSchema.getUsername();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
